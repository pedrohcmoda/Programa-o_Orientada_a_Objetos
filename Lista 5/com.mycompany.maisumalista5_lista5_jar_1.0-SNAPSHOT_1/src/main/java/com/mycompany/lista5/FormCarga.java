    public void tabelaCarga(){
        DefaultTableModel tabModel = (DefaultTableModel) tableCarga.getModel();
        int posLin = 0;
        tabModel.setRowCount(posLin);
      
        for(Carga p : Banco.getBanco().getBdCarga()){
            tabModel.insertRow(posLin, new Object[]{p.getPlaca(), p.getMarca(), p.getModelo(), p.getCor(), p.getQtdRoda(), p.calcVel(p.getVelocMax()), p.getMotor().getQtdPistoes(), p.getMotor().getPotencia(), p.getTara(), p.getCargaMax(), p.getDataCadastro(), p.calcular()});
            posLin++;
        }
    }
